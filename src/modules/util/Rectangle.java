package modules.util;

public class Rectangle {

	public float x = 0;
	public float y = 0;
	public float width = 0;
	public float height = 0;
	
	public Rectangle(float x, float y, float width, float height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
	public Rectangle() {
		
	}
	
}
